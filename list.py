# การสร้าง list ใน python
numbers = [20, 32, 89, 95, 130]
names = ['John', 'Joe', 'Jim', 'Jane']
mixed = [-2, 5, 84.2, "Mountain", "Python"]

# การแสดงข้อมูลใน list
print(numbers[0])
print(numbers[4])
print(names[3])
print(mixed[3])

# การวนลูปข้อมูลสมาชิกใน list
for num in numbers:
    print(num)

for name in names:
    print(name)

for mix in mixed:
    print(mix)
